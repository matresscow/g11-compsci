public class Liu_Max_Pg72Ex6 {

    /**
     * This program displays the square, square root, and reciprocal of the
     * integers from 1 to 40.
     */
    public static void main(String[] args) {
        //do this for the numbers from 1-40, 40 times
        for (double star = 1; star <= 40; star++) {
            //print out the square, square root, and reciprocal of the number
            System.out.print("The square, square root, and reciprocal of " + (int)star + " are: \t");
            System.out.println("Square root: " + Math.sqrt(star) + "\tsquare: " + (star * star) + "\t reciprocal: " + (1 / star));
            if (star % 5 == 0) {
                //Every 5 lines, print out an empty line to make the table took better.
                System.out.println("");
            }
        }
    }
}
