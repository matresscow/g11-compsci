public class Liu_Max_Reverse {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //Create the array of 10 elements
        int[] tie = new int[10];
        //Countdown
        System.out.println("Countdown");
        //set the element as its index, then display it.
        for (int i = tie.length - 1; i >= 0; i--) {
            tie[i] = i;
            System.out.println(tie[i]);
        }

    }
}
