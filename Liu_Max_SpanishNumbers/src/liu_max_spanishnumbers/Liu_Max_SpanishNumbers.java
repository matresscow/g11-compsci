package liu_max_spanishnumbers;

/**
 * This program displays the numbers from 1-10 in Spanish.
 */
public class Liu_Max_SpanishNumbers {

    //Use a switch statement to determine what to print out.
    static void translate(int n) {
        switch (n) {
            case 1:
                System.out.println("uno");
                break;
            case 2:
                System.out.println("dos");
                break;
            case 3:
                System.out.println("tres");
                break;
            case 4:
                System.out.println("cuatro");
                break;
            case 5:
                System.out.println("cinco");
                break;
            case 6:
                System.out.println("seis");
                break;
            case 7:
                System.out.println("siete");
                break;
            case 8:
                System.out.println("ocho");
                break;
            case 9:
                System.out.println("nueve");
                break;
            case 10:
                System.out.println("diez");
                break;
            default:
                System.out.println("No.");
        }
    }
    
    //Use a for statement to call it 10 times.
    public static void main(String[] args) {
        for (byte i = 1; i <= 10; i++) {
            //add the arabic numeral and an indent so it looks good.
            System.out.print(i + "\t");
            translate(i);
        }
    }
}
